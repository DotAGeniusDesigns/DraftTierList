// Helper function to get team data by abbreviation
export const getTeamData = (teamAbbr) => {
    return teamData[teamAbbr] || null;
};

// Helper function to get team logo by abbreviation
export const getTeamLogo = (teamAbbr) => {
    const team = teamData[teamAbbr];
    return team ? team.logo : null;
};

// Helper function to get bye week by abbreviation
export const getByeWeek = (teamAbbr) => {
    const team = teamData[teamAbbr];
    return team ? team.byeWeek : null;
};

// Helper function to get all teams
export const getAllTeams = () => {
    return Object.keys(teamData).map(abbr => ({
        abbreviation: abbr,
        ...teamData[abbr]
    }));
}; 